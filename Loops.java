
public class Loops {

	public static void main(String[] args) {
		System.out.println("---For Loop---");
		for(int num = 1 ; num <= 10 ; num++) {
			System.out.println(num);
		}
		
		System.out.println("\n---While Loop---");
		int num = 1;
		while(num <= 10) {
			System.out.println(num);
			num = num + 1;
		}

	}

}
